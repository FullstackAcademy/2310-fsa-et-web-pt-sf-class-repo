// Logical Operators

// AND
// Uses &&, only true if both sides are true
console.log('true && true', true && true);

// OR
// Uses ||, only true if one or both sides is true
console.log('true || false', true || false);

// NOT
// Uses !, this inverts the value after it
console.log('!false', !false);

